    /**
     * 检测权限
     *
     * @param context
     * @param permission
     * @return
     */
    public static boolean checkPermission(Context context, String permission) {
        boolean result = false;
        if (context == null) {
            return result;
        }
        if (Build.VERSION.SDK_INT >= 23) {
            try {
                Class<?> clazz = Class.forName("android.content.Context");
                Method method = clazz.getMethod("checkSelfPermission", String.class);
                int rest = (Integer) method.invoke(context, permission);
                if (rest == PackageManager.PERMISSION_GRANTED) {
                    result = true;
                } else {
                    result = false;
                }
            } catch (Throwable e) {
                result = false;
            }
        } else {
            PackageManager pm = context.getPackageManager();
            if (pm.checkPermission(permission, context.getPackageName()) == PackageManager.PERMISSION_GRANTED) {
                result = true;
            }
        }
        return result;
    }

    /**
     * 检测是否是虚拟机
     */
    public static boolean isEmulator(Context context) {
        // 判断是否原生模拟器
        if (Build.MODEL.equalsIgnoreCase("sdk") || Build.MODEL.equalsIgnoreCase("google_sdk")) {
            return true;
        }

        // 判断是否平板和imei号为空
        try {
            if (checkPermission(context, Manifest.permission.READ_PHONE_STATE)) {
                TelephonyManager tm = (TelephonyManager) context.getSystemService(Context.TELEPHONY_SERVICE);
                @SuppressLint("MissingPermission") String imei = tm.getDeviceId();
                if (!isTablet(context) && TextUtils.isEmpty(imei)) {
                    return true;
                }
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }

        return false;

    }

    /**
     * 判断是否是平板
     *
     * @param context
     * @return
     */
    public static boolean isTablet(Context context) {
        return (context.getResources().getConfiguration().screenLayout & Configuration.SCREENLAYOUT_SIZE_MASK) >=
                Configuration.SCREENLAYOUT_SIZE_LARGE;
    }

    /**
     * 获取硬件架构
     *
     * @return
     */
    public static String getCpuAbi() {
        if (Build.VERSION.SDK_INT < 21) {
            // 硬件架构
            String abi = Build.CPU_ABI;
            abi = (abi == null || abi.trim().length() == 0) ? "" : abi;
            // 检视是否有第二类型
            String cpuAbi2 = null;
            try {
                cpuAbi2 = Build.class.getField("CPU_ABI2").get(null).toString();

            } catch (Throwable e) {
                e.printStackTrace();

            }
            cpuAbi2 = (cpuAbi2 == null || cpuAbi2.trim().length() == 0) ? null : cpuAbi2;
            if (cpuAbi2 != null) {
                abi = abi + "|" + cpuAbi2;
            }

            return abi;

        } else {
            StringBuilder abi = new StringBuilder();
            String[] supportedAbis = Build.SUPPORTED_ABIS;
            if (supportedAbis != null && supportedAbis.length > 0) {
                for (int i = 0; i < supportedAbis.length; ++i) {
                    if (i == (supportedAbis.length - 1)) {
                        abi.append(supportedAbis[i]);

                    } else {
                        abi.append(supportedAbis[i]).append("|");
                    }
                }
            }

            return abi.toString();
        }
    }

